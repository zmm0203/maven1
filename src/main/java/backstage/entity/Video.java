package backstage.entity;


public class Video {

  private long vId;
  private String vName;
  private String vIntroduce;
  private long vPlaytimes;
  private String vVideo;
  private String vImage;
  private String vComment;


  public long getVId() {
    return vId;
  }

  public void setVId(long vId) {
    this.vId = vId;
  }


  public String getVName() {
    return vName;
  }

  public void setVName(String vName) {
    this.vName = vName;
  }


  public String getVIntroduce() {
    return vIntroduce;
  }

  public void setVIntroduce(String vIntroduce) {
    this.vIntroduce = vIntroduce;
  }


  public long getVPlaytimes() {
    return vPlaytimes;
  }

  public void setVPlaytimes(long vPlaytimes) {
    this.vPlaytimes = vPlaytimes;
  }


  public String getVVideo() {
    return vVideo;
  }

  public void setVVideo(String vVideo) {
    this.vVideo = vVideo;
  }


  public String getVImage() {
    return vImage;
  }

  public void setVImage(String vImage) {
    this.vImage = vImage;
  }


  public String getVComment() {
    return vComment;
  }

  public void setVComment(String vComment) {
    this.vComment = vComment;
  }

}
